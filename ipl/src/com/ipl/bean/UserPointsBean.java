package com.ipl.bean;

public class UserPointsBean
{

    private int userId;
    
    private String userName;
    
    private int userPoints;
    
    private int played;
    
    private int wonCount;
    
    private int lostCount;
    
    private int drawCount;
    
    private String indPoints;
   
    private String indMatch;
    
    public String getIndPoints() {
		return indPoints;
	}

	public void setIndPoints(String indPoints) {
		this.indPoints = indPoints;
	}

	public String getIndMatch() {
		return indMatch;
	}

	public void setIndMatch(String indMatch) {
		this.indMatch = indMatch;
	}

	public int getUserId() {
		return userId;
	}

	public void setUserId(int userId) {
		this.userId = userId;
	}

	public int getUserPoints() {
		return userPoints;
	}

	public void setUserPoints(int userPoints) {
		this.userPoints = userPoints;
	}

	public int getPlayed() {
		return played;
	}

	public void setPlayed(int played) {
		this.played = played;
	}

	public int getWonCount() {
		return wonCount;
	}

	public void setWonCount(int wonCount) {
		this.wonCount = wonCount;
	}

	public int getLostCount() {
		return lostCount;
	}

	public void setLostCount(int lostCount) {
		this.lostCount = lostCount;
	}

	public int getDrawCount() {
		return drawCount;
	}

	public void setDrawCount(int drawCount) {
		this.drawCount = drawCount;
	}

	public String getUserName() {
		return userName;
	}

	public void setUserName(String userName) {
		this.userName = userName;
	}
	
	
    
}
